velho = int(input("Digite sua idade: "))
cpf = int(input("Digite seu CPF: "))
per = int(input("Digite em qual período você se encontra na graduação: "))

print("Sua idade em binário é {0:b}".format(velho))
print("Seu CPF em Hexa é {0:x}".format(cpf))
